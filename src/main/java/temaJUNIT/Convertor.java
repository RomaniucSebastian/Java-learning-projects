package temaJUNIT;
 // Clasa cu dimensiuni

public class Convertor {

    private int mm = 1;
    private int cm = 10;
    private int dm = 100;
    private int m = 1000;
    private int km = 1000000;

    public int getMm() {
        return mm;
    }

    public int getCm() {

        return cm;
    }

    public int getDm() {

        return dm;
    }

    public int getM() {

        return m;
    }

    public int getKm() {

        return km;
    }
}